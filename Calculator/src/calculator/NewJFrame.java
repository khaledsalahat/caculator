    private void convertNumber() {
        String input = jTextField1.getText();
        int base = Base();
    
        if (base == -1 || !isValidInput(input, base)) {
            Invalid();
            return;
        }

        int decimalValue = Integer.parseInt(input, base);
        jLabel8.setText(Integer.toBinaryString(decimalValue));
        jLabel9.setText(Integer.toString(decimalValue));
        jLabel10.setText(Integer.toOctalString(decimalValue));
        jLabel11.setText(Integer.toHexString(decimalValue).toUpperCase());
}

    private boolean isValidInput(String input, int base) {
    switch (base) {
        case 2:
            return isValidBinary(input);
        case 10:
            return isValidDecimal(input);
        case 8:
            return isValidOctal(input);
        case 16:
            return isValidHex(input);
        default:
            return false;
    }
}
    private void Invalid() {
        jLabel8.setText("Invalid input");
        jLabel9.setText("Invalid input");
        jLabel10.setText("Invalid input");
        jLabel11.setText("Invalid input");
    }

    private int Base() {
               if (jRadioButton1.isSelected()) {
            return 2;
        } else if (jRadioButton2.isSelected()) {
            return 10;
        } else if (jRadioButton3.isSelected()) {
            return 8;
        } else if (jRadioButton4.isSelected()) {
            return 16;
        }
         return 0;
    }


