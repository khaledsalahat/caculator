    private boolean isValidBinary(String input) {
         return input.matches("[01]+");
}

    private boolean isValidDecimal(String input) {
         return input.matches("\\d+");
}

    private boolean isValidOctal(String input) {
         return input.matches("[0-7]+");
}

    private boolean isValidHex(String input) {
         return input.matches("[0-9A-Fa-f]+");
}

